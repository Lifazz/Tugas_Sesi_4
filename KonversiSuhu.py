reamur = float(input(" Masukan Suhu  : "))
celcius = 0.0
fahrenheit = 0.0
kelvin =  0.0

celcius = 5/4 * reamur
fahrenheit = 9/4 * reamur + 32
kelvin = 5/4 * reamur + 273.15

print(" Suhu  Reamur %.2f -> Celcius : %.2f"%(reamur,celcius))
print(" Suhu  Reamur %.2f -> Fahrenheit : %.2f"%(reamur,fahrenheit))
print(" Suhu Reamur %.2f -> Kelvin : %.2f"%(reamur,kelvin))